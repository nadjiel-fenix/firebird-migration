  APROVADO = 'S';
  if (not exists(select CODIGO from PEDVEND_CRITICADOS where CODPEDVEND = :ECODPEDVEND and TIPO_CRITICA = :ECODANALISECRITERIO and USUARIO_LIBEROU is not null)) then
  begin
