  IF (EXISTS(SELECT 1 FROM DFL_CONSULTA WHERE DFL_CONSULTA.CONSULTA = 'DFL_FABRICANTE_UI')) THEN
  BEGIN
    SELECT SQL FROM DFL_CONSULTA WHERE DFL_CONSULTA.CONSULTA = 'DFL_FABRICANTE_UI' INTO LSQL;
