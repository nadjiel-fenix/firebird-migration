ALTER PROCEDURE CORRIGE_IDREGISTRO_MOVESTOQUE AS 
declare variable LIDREGISTROITEM integer;
declare variable LCODPEDVEND integer;
begin
  LIDREGISTROITEM = 0;
  LCODPEDVEND     = 0;

  for select
    M.IDREGISTROITEM
  from
    MOVESTOQUE M
  where
    M.TIPODOC    = 'PV' and
    M.IDREGISTRO = 0
  into
    LIDREGISTROITEM
  do
  begin
    select
      coalesce(PVI.CODPEDVEND, 0)
    from
      PEDVENDITEM PVI
    where
      PVI.CODPEDVENDITEM = :LIDREGISTROITEM
    into
      LCODPEDVEND;

    update
      MOVESTOQUE
    set
      MOVESTOQUE.IDREGISTRO = :LCODPEDVEND
    where
      MOVESTOQUE.IDREGISTRO     = 0 and
      MOVESTOQUE.IDREGISTROITEM = :LIDREGISTROITEM and
      MOVESTOQUE.TIPODOC        = 'PV';
  end

  LIDREGISTROITEM = 0;
  LCODPEDVEND     = 0;

  for select
    M.IDREGISTROITEM
  from
    MOVESTOQUE M
  where
    M.TIPODOC    = 'NV' and
    M.IDREGISTRO = 0
  into
    LIDREGISTROITEM
  do
  begin
    select
      coalesce(NVI.CODNFVENDA, 0)
    from
      NFVENDAITEM NVI
    where
      NVI.CODPEDVENDITEM = :LIDREGISTROITEM
    into
      LCODPEDVEND;

    update
      MOVESTOQUE
    set
      MOVESTOQUE.IDREGISTRO = :LCODPEDVEND
    where
      MOVESTOQUE.IDREGISTRO     = 0 and
      MOVESTOQUE.IDREGISTROITEM = :LIDREGISTROITEM and
      MOVESTOQUE.TIPODOC        = 'PV';
  end
  exit;
end^

ALTER PROCEDURE DELETA_BORDERO (CODBORDERO INTEGER)
AS 
declare variable CODBORDEROPAGAMENTO integer;
begin
  for select
    CODBORDEROPAGAMENTO
  from
    BORDEROPAGAMENTO
  where
    BORDEROPAGAMENTO.CODBORDEROPAGAMENTO = :CODBORDERO
  into
    :CODBORDEROPAGAMENTO
  do
  begin
    delete from
      BORDEROPAGAMENTODEBITO
    where
      CODBORDEROPAGAMENTO = :CODBORDERO;
  
    delete from
      BORDEROPAGAMENTOTITULO
    where
      CODBORDEROPAGAMENTO = :CODBORDERO;
  
    delete from
      BORDEROPAGAMENTO
    where
      CODBORDEROPAGAMENTO = :CODBORDERO;
    exit;
  end
end^

